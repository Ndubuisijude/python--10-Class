"""
a simple class called aptech that prints "I'm ADSE", "I'm web dev", "I'm a student" and "I'm not a student".
"""

class Aptech:
    def im_adse(self):
        print("I'm ADSE")
    def im_webdev(self):
        print("I'm web dev")
    def im_student(self):
        print("I'm a student")
    def im_not_student(self):
        print("I'm not a student")
aptech = Aptech()
aptech.im_adse()
aptech.im_webdev()
aptech.im_student()
aptech.im_not_student()
